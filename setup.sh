export default defineConfig({
  plugins: [react()],
  resolve: {
    alias: {
      "@": path.resolve(__dirname, "./src"),
    },
  },
});
EOF

# Extract components
echo "Extracting shadcn components..."
tar -xzf .trae/skills/web-artifacts-builder/scripts/shadcn-components.tar.gz -C src/

# Create components.json
echo "Creating components.json..."
