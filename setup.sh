# Path aliases in tsconfig.json
echo "Configuring tsconfig.json..."
node -e "
const fs = require('fs');
const config = JSON.parse(fs.readFileSync('tsconfig.json', 'utf8'));
config.compilerOptions = config.compilerOptions || {};
config.compilerOptions.baseUrl = '.';
config.compilerOptions.paths = { '@/*': ['./src/*'] };
fs.writeFileSync('tsconfig.json', JSON.stringify(config, null, 2));
"

# Path aliases in tsconfig.app.json
echo "Configuring tsconfig.app.json..."
node -e "
const fs = require('fs');
const path = 'tsconfig.app.json';
if (fs.existsSync(path)) {
  const content = fs.readFileSync(path, 'utf8');
  // Remove comments roughly
  const lines = content.split('\n').filter(line => !line.trim().startsWith('//'));
  const jsonContent = lines.join('\n').replace(/\/\*[\s\S]*?\*\//g, '');
  try {
    // Attempt to parse JSON (might fail if trailing commas, etc.)
    // But create-vite templates are usually standard JSON or JSONC
    // Using a safer approach: eval if possible? No, eval is dangerous.
    // Let's assume valid JSON for now.
    const config = JSON.parse(jsonContent);
    config.compilerOptions = config.compilerOptions || {};
    config.compilerOptions.baseUrl = '.';
    config.compilerOptions.paths = { '@/*': ['./src/*'] };
    fs.writeFileSync(path, JSON.stringify(config, null, 2));
  } catch (e) {
    console.error('Failed to parse tsconfig.app.json', e);
  }
}
"

# Vite config
echo "Configuring vite.config.ts..."
cat > vite.config.ts << 'EOF'
import path from "path";
import react from "@vitejs/plugin-react";
